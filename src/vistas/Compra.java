    public void Mostrar(String ID){
    DefaultTableModel modelo=new DefaultTableModel();
    modelo.addColumn("ID");
    modelo.addColumn("ID Producto");
    modelo.addColumn("ID Proveedor");    
    modelo.addColumn("ID Empleado");
    modelo.addColumn("Fecha De Registro");
    modelo.addColumn("Cantidad Productos");
    modelo.addColumn("Metodo de Pago");
    modelo.addColumn("Total");
    TablaCompra.setModel(modelo);
    String sql="";
    if(ID.equals("")){
        sql="Select*from Compra";
    }else{
        sql="Select*from Compra where IdCompra like'%"+ID+"%'";
    }
    String Compra[]=new String[8];
    java.sql.Statement set;
    try {
        set = conectar.createStatement(); 
        ResultSet resul=set.executeQuery(sql);
        while(resul.next()){
            Compra[0]=resul.getString(1);
              Compra[1]=resul.getString(2);
                Compra[2]=resul.getString(3);  
                 Compra[3]=resul.getString(4);
                  Compra[4]=resul.getString(5);
                    Compra[5]=resul.getString(6);
                     Compra[6]=resul.getString(7);
                      Compra[7]=resul.getString(8);
                    modelo.addRow(Compra);
                        
        }
            TablaCompra.setModel(modelo);
        } catch (SQLException ex) {
        java.util.logging.Logger.getLogger(Compra.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    
    }
    
    public void Guardar(){
        String Id=ID.getText(),IdProducto=IDP.getText(),IdProveedor=IDProveedor.getText(),IDEmpleado=IDE.getText(),Fecha=txtFecha.getText(),
                Cantidad=txtCantidad.getText(),Pago=txtPago.getText(),Total=txtTotal.getText();
        String Sql="Insert into Compra() Values(?,?,?,?,?,?,?,?)";
        try{
            Statement set=(Statement)conectar.createStatement();
            ResultSet resultado=set.executeQuery("Select*from Compra where IdCompra like'"+ID.getText()+"'");
            if(resultado.next()){
                getToolkit().beep();
                JOptionPane.showMessageDialog(null,"La Compra ya existe");
                ID.requestFocus();
            }else if(ID.getText().isEmpty()){
                getToolkit().beep();
                JOptionPane.showMessageDialog(null,"Id Obligatorio");
                ID.requestFocus();
            }else{
                PreparedStatement pasar =conectar.prepareStatement(Sql);
                pasar.setString(1, Id);
                pasar.setString(2, IdProducto);
                pasar.setString(3, IdProveedor);
                pasar.setString(4, IDEmpleado);
                pasar.setString(5, Fecha);
                pasar.setString(6, Cantidad);
                pasar.setString(7, Pago);
                pasar.setString(8, Total);
                pasar.executeUpdate();
                JOptionPane.showMessageDialog(null,"Registro Exitoso");
        }
        }catch (SQLException ex){
            java.util.logging.Logger.getLogger(Compra.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }
    
