    public void ModificarDatos(){
    try {
        PreparedStatement pasar=conectar.prepareStatement("Update Compra set id_Productos='"+IDP.getText()+"',id_Proveedor='"+IDProveedor.getText()+"',id_Empleado='"+IDE.getText()
                +"',FechaDeRegistro='"+txtFecha.getText()+"',CantidadProductos='"+txtCantidad.getText()+"',MetoDePago='"+txtPago.getText()+"',Total='"+txtTotal.getText()+"' where IdCompra='"+ID.getText()+"'");
        pasar.executeUpdate();
        Mostrar("");
        
    } catch (SQLException ex) {
        java.util.logging.Logger.getLogger(Compra.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
    }
    }
    
    public void Borrar(){
    int fila=TablaCompra.getSelectedRow();
    String ID=TablaCompra.getValueAt(fila,0).toString();
    int n=JOptionPane.showConfirmDialog(null,"Desea eliminar registro","Registro",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
    if(n==JOptionPane.YES_NO_OPTION){
    try {
        PreparedStatement borrar=conectar.prepareStatement("Delete from Compra where IdCompra='"+ID+"'");
        borrar.executeUpdate();
        Mostrar("");
    } catch (SQLException ex) {
        java.util.logging.Logger.getLogger(Compra.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }
    
    }
